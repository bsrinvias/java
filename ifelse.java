package com.java.first;

public class ifelse {

	public static void main(String[] args) {
	
		int age=65;
		if(age<=18)
		{
			System.out.println("not ok");
		}
		else if(age>18 && age<60)
		{
			System.out.println("good");
			
		}
		else{
			System.out.println("ok");
		}

	}

	
}
